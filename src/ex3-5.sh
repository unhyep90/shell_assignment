#!/bin/bash

# 1. 내부 함수 정의: run_ls_with_options

# 이 함수는 스크립트로 전달된 인자들을 받아와 ls 명령어의 옵션으로 사용
# 인자들은 $1, $2 또는 한 번에 $@ 변수를 통해 접근 가능
run_ls_with_options() {
    echo "--- [함수 시작] 'ls' 명령어 실행 ---"
    echo "함수에 전달된 인자(옵션): $@"  # $@는 함수에 전달된 모든 인자를 의미

    # eval 명령어를 사용하여 "ls -l -a"와 같은 문자열을 하나의 완성된 쉘 명령어로 인식하고 실행
    # $1, $2 와 같은 인자를 명령어에 전달
    eval "ls $@"
    
    echo "--- [함수 종료] ---"
    echo ""
}
# 2. 메인 스크립트 실행

echo "▶ 스크립트 이름: $0"
echo "▶ 전체 입력 인자: $@" # $@는 스크립트 실행 시 입력된 모든 인자를 의미
echo ""

# 3. 스크립트 입력 인자($@)를 내부 함수에 전달하여 실행

run_ls_with_options "$@"

exit 0